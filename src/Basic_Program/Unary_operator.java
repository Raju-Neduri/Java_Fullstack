package Basic_Program;

public class Unary_operator {
	public static void main(String[] args) {
		int x=10;
		++x;
		System.out.println("After first preincrement:" +x);
		++x;
		System.out.println("After Second preincrement:" +x);
		++x;
		System.out.println("After third preincrement :" +x);
		++x;
	}

}
