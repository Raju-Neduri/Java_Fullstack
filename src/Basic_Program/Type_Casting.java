package Basic_Program;

public class Type_Casting {

	public static void main(String[] args) {
		int i=100;
		long l=i;
		System.out.println("Value of i=" +i+ " Value of l=" + l);
		short s=9;
		int k=s;
		System.out.println("Value of s=" +s+ " Value of i=" + k);
		
		long l1=900;
		float f1=l1;
		System.out.println("Value of l1=" +l1+ " Value of f1=" + f1);
		
		int i1=789;
		float f2=i1;
		System.out.println("Value of i1=" +i1+ " Value of f2=" + f2);
		
		float f=7.8f;
		double d=f;
		System.out.println("Value of f=" +f+ " Value of d=" + d);
		
		
	
}
}