package practice_problems;

public class AverageMarks {

	    public static void main(String[] args) {
	        int m1 = 78;
	        int m2 = 72;
	        int m3 = 56;
	        int m4 = 90;
	        int m5 = 45;
	        int totalMarks = m1 + m2 + m3 + m4 + m5;
	        float average = totalMarks / 5.0f;
	        System.out.println("The average of the marks is: " + average);
	    }
	}


